#pragma once

#include "bsp.hpp" 

// ============================================================
// BL24C16F EEPROM 驱动类
// BL24C16F: 16Kbit (2048 × 8bit) I2C EEPROM
//   - 地址范围: 0x000 ~ 0x7FF (11位地址)
//   - 页写入: 16字节/页
//   - 器件地址(7bit): 0x50
//   - I2C 速率: Fast Mode (400kHz)
//   - 内部写入时间: ~5ms
//
// 使用方式:
//   BL24C16F::init();
//   BL24C16F::write_byte(0x0100, 0xAB);
//   uint8_t val = BL24C16F::read_byte(0x0100);
//   BL24C16F::write(0x0000, buf, 32);
//   BL24C16F::read(0x0000, buf, 32);
// ============================================================

class BL24C16F
{
public:
    // ============================================================
    // 常量定义
    // ============================================================
    static const uint16_t EEPROM_SIZE   = 2048;     // 总容量 2048 字节
    static const uint16_t PAGE_SIZE     = 16;       // 页大小 16 字节
    static const uint8_t  DEV_ADDR      = 0x50;     // 7位I2C器件地址(基址)
    static const uint16_t WRITE_CYCLE_MS = 5;       // 内部写入时间 5ms

    // ============================================================
    // 初始化
    // 探测 EEPROM 是否在线，返回 true=正常 false=未响应
    // ============================================================
    static bool init(void);

    // ============================================================
    // 读取一个字节
    // @addr: EEPROM 内部地址 (0x000 ~ 0x7FF)
    // @return: 读取到的字节值
    // ============================================================
    static uint8_t read_byte(uint16_t addr);

    // ============================================================
    // 写入一个字节
    // @addr: EEPROM 内部地址 (0x000 ~ 0x7FF)
    // @data: 要写入的字节
    // @return: true=写入成功 false=写入失败
    // ============================================================
    static bool write_byte(uint16_t addr, uint8_t data);

    // ============================================================
    // 连续读取多个字节
    // @addr:  起始地址
    // @buf:   存放读取数据的缓冲区
    // @len:   读取长度 (字节数)
    // @return: true=读取成功 false=读取失败
    // ============================================================
    static bool read(uint16_t addr, uint8_t *buf, uint16_t len);

    // ============================================================
    // 连续写入多个字节（自动处理页边界）
    // @addr:  起始地址
    // @buf:   待写入数据的缓冲区
    // @len:   写入长度 (字节数)
    // @return: true=写入成功 false=写入失败
    // ============================================================
    static bool write(uint16_t addr, const uint8_t *buf, uint16_t len);

private:
    // ============================================================
    // 将线性地址转换为器件地址和页内偏移
    // BL24C16F 的 2048 字节分为 8 页 (256字节/页)
    // 器件地址 = 0x50 | block (block = 0~7)
    // 页内偏移 = addr & 0xFF
    // ============================================================
    static uint8_t _dev_addr(uint16_t addr);
    static uint8_t _page_offset(uint16_t addr);

    // ============================================================
    // 写入一页数据（不超过页边界）
    // @dev_addr:  器件地址（含 block select）
    // @offset:    页内偏移
    // @buf:       数据缓冲区
    // @len:       写入长度 (≤ PAGE_SIZE - offset_in_page)
    // ============================================================
    static bool _write_page(uint8_t dev_addr, uint8_t offset,
                            const uint8_t *buf, uint16_t len);

    // ============================================================
    // 等待内部写入完成（ACK 轮询，超时后返回）
    // ============================================================
    static void _wait_write_complete(void);
};
